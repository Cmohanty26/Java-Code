public class SwitchProgram2 {
    public static void main(String[] args) {
        char myLetter='p';
        switch (Character.toUpperCase(myLetter)){
            case 'A':
                System.out.println("It is vowel");
                break;
            case 'E':
                System.out.println("It is vowel");
                break;
            case 'I':
                System.out.println("It is vowel");
                break;
            case 'O':
                System.out.println("It is vowel");
                break;
                case 'U':
                System.out.println("It is vowel");
                break;
            default:
                System.out.println("It is not a vowel");

        }
    }
}
