package com.Condition;
public class Example2 {

	public static void main(String[] args) {
		char ch='A';
		if(ch=='A') {
			System.out.println("Apple");
		}
		else if(ch=='B') {
			System.out.println("Boy");
		}
		else if(ch=='C') {
			System.out.println("Cat");
		}
		else {
			System.out.println("Orange");
		}
	}

}
